class Message:
    
    def __init__(self, id: int, data: str):
        self._data = data
        self._id = id
        self._message: str = None
        return
    
    def buildMessage(self):
        self._message = f"#{self._id} " + self._data
        return self
    
    def deliver(self):
        print(f"{self._message}")
        return


if __name__ == '__main__':
    
    m = Message(1, "LALELU")
    m.buildMessage().deliver()
